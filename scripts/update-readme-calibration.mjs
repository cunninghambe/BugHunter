#!/usr/bin/env node
// CI helper: rewrite the <!-- BEGIN CALIBRATION --> block in README.md.
// Usage: node scripts/update-readme-calibration.mjs <aggregate.json>

import * as fs from 'node:fs';
import * as path from 'node:path';
import * as child_process from 'node:child_process';

const aggregatePath = process.argv[2];
if (!aggregatePath || !fs.existsSync(aggregatePath)) {
  process.stderr.write(`Usage: update-readme-calibration.mjs <aggregate.json>\n`);
  process.exit(1);
}

const agg = JSON.parse(fs.readFileSync(aggregatePath, 'utf-8'));
const { overall, perKind, appsIncluded, generatedAt, bughunterCommit } = agg;

// Resolve bench commit SHA if possible
let benchCommit = 'unknown';
try {
  const r = child_process.spawnSync('git', ['-C', '/tmp/bench', 'rev-parse', '--short', 'HEAD'], { encoding: 'utf-8' });
  benchCommit = r.stdout.trim() || 'unknown';
} catch { /* ignore */ }

const goldCount = Object.values(perKind ?? {}).reduce((s, v) => s + v.tp + v.fn + v.tn, 0);
const appCount = appsIncluded?.length ?? 0;

const tableRows = Object.entries(perKind ?? {})
  .filter(([, v]) => v.registryStatus !== 'deferred' && v.tp + v.fn > 0)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([kind, v]) => {
    const appsCount = v.appsCovered?.length ?? '?';
    return `| ${kind.padEnd(38)} | ${String(v.precision).padStart(9)} | ${String(v.recall).padStart(6)} | ${String(v.f1).padStart(4)} | ${appsCount}    |`;
  });

const block = [
  '<!-- BEGIN CALIBRATION -->',
  `## Calibration (last updated ${generatedAt?.slice(0, 10)}; bench v${agg.overall?.benchVersion ?? '?'}; ${appCount} apps; ${goldCount} gold entries; bench@${benchCommit})`,
  '',
  `**Overall**: precision=${overall.precision} recall=${overall.recall} f1=${overall.f1} (tp=${overall.tp} fp=${overall.fp} fn=${overall.fn})`,
  '',
  '| BugKind                                 | Precision | Recall | F1   | Apps |',
  '|-----------------------------------------|-----------|--------|------|------|',
  ...tableRows,
  '',
  `[View raw report](https://github.com/cunninghambe/BugHunter/actions) · Generated by bughunter@${bughunterCommit}`,
  '<!-- END CALIBRATION -->',
].join('\n');

const readmePath = path.join(path.dirname(new URL(import.meta.url).pathname), '..', 'README.md');
if (!fs.existsSync(readmePath)) {
  process.stderr.write(`README.md not found at ${readmePath}\n`);
  process.exit(1);
}

const readme = fs.readFileSync(readmePath, 'utf-8');
const begin = '<!-- BEGIN CALIBRATION -->';
const end = '<!-- END CALIBRATION -->';
const beginIdx = readme.indexOf(begin);
const endIdx = readme.indexOf(end);

let updated;
if (beginIdx !== -1 && endIdx !== -1) {
  updated = readme.slice(0, beginIdx) + block + readme.slice(endIdx + end.length);
} else {
  // Append at end if block is missing
  updated = `${readme}\n\n${block}\n`;
}

fs.writeFileSync(readmePath, updated);
process.stdout.write(`README.md calibration block updated.\n`);
